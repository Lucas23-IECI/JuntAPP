-- JuntAPP Database Initialization Schema
-- Created on Mayo 2026

-- Enable UUID extension if not present
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- ==========================================
-- 👥 1. PROFILES / SOCIOS TABLE
-- ==========================================
CREATE TABLE public.profiles (
    id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
    name TEXT NOT NULL,
    rut TEXT UNIQUE NOT NULL,
    address TEXT NOT NULL,
    phone TEXT,
    email TEXT UNIQUE NOT NULL,
    role TEXT NOT NULL CHECK (role IN ('vecino', 'dirigente')) DEFAULT 'vecino',
    cuota_status TEXT NOT NULL CHECK (cuota_status IN ('al_dia', 'pendiente')) DEFAULT 'pendiente',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Enable Row Level Security
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

-- Profiles Policies
CREATE POLICY "Public profiles are readable by authenticated users" 
ON public.profiles FOR SELECT 
TO authenticated 
USING (true);

CREATE POLICY "Profiles are updatable by dirigentes or owner" 
ON public.profiles FOR UPDATE 
TO authenticated 
USING (
    (SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente'
    OR id = auth.uid()
) 
WITH CHECK (
    (SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente'
    OR id = auth.uid()
);

CREATE POLICY "Profiles can be inserted by dirigentes or registration" 
ON public.profiles FOR INSERT 
TO authenticated 
WITH CHECK (true); -- Registrations are managed via trigger or client

CREATE POLICY "Profiles can be deleted by dirigentes" 
ON public.profiles FOR DELETE 
TO authenticated 
USING ((SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente');


-- ==========================================
-- 💵 2. TRANSACTIONS / LIBRO DE CAJA TABLE
-- ==========================================
CREATE TABLE public.transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type TEXT NOT NULL CHECK (type IN ('ingreso', 'egreso')),
    description TEXT NOT NULL,
    amount NUMERIC(12, 2) NOT NULL CHECK (amount > 0),
    date DATE NOT NULL DEFAULT CURRENT_DATE,
    created_by UUID REFERENCES public.profiles(id) ON DELETE SET NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Enable Row Level Security
ALTER TABLE public.transactions ENABLE ROW LEVEL SECURITY;

-- Transactions Policies
CREATE POLICY "Transactions are readable by authenticated users" 
ON public.transactions FOR SELECT 
TO authenticated 
USING (true);

CREATE POLICY "Transactions can be inserted by dirigentes" 
ON public.transactions FOR INSERT 
TO authenticated 
WITH CHECK ((SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente');

CREATE POLICY "Transactions can be updated by dirigentes" 
ON public.transactions FOR UPDATE 
TO authenticated 
USING ((SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente')
