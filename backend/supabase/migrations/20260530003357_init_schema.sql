-- ==========================================
-- 🔔 6. NOTIFICATIONS / AVISOS TABLE
-- ==========================================
CREATE TABLE public.notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES public.profiles(id) ON DELETE CASCADE,
    type TEXT NOT NULL CHECK (type IN ('asamblea', 'votacion', 'cuota', 'seguridad')),
    title TEXT NOT NULL,
    message TEXT NOT NULL,
    read BOOLEAN NOT NULL DEFAULT false,
    date TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    action TEXT,
    target_socio_id BIGINT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- Enable Row Level Security
ALTER TABLE public.notifications ENABLE ROW LEVEL SECURITY;

-- Notifications Policies
CREATE POLICY "Users can view and update their own notifications" 
ON public.notifications FOR ALL 
TO authenticated 
USING (user_id = auth.uid())
WITH CHECK (user_id = auth.uid());

CREATE POLICY "System or Dirigentes can insert notifications" 
ON public.notifications FOR INSERT 
TO authenticated 
WITH CHECK (
    (SELECT role FROM public.profiles WHERE id = auth.uid()) = 'dirigente'
    OR auth.uid() IS NOT NULL -- Allow system functions
);


-- ==========================================
-- 🔄 7. TRIGGERS: SYNC AUTH USERS -> PROFILES
-- ==========================================

-- Function to handle new user registrations
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
DECLARE
    default_role TEXT := 'vecino';
    default_cuota TEXT := 'pendiente';
BEGIN
    -- Determine role by email domain or metadata if needed
    -- For convenience, admin@juntapp.cl is seeded as 'dirigente'
    IF NEW.email = 'admin@juntapp.cl' OR NEW.email = 'dirigente@juntapp.cl' THEN
        default_role := 'dirigente';
        default_cuota := 'al_dia';
    END IF;

    INSERT INTO public.profiles (id, name, rut, address, phone, email, role, cuota_status)
    VALUES (
        NEW.id,
        COALESCE(NEW.raw_user_meta_data->>'name', 'Nuevo Vecino'),
        COALESCE(NEW.raw_user_meta_data->>'rut', '11.111.111-1'),
        COALESCE(NEW.raw_user_meta_data->>'address', 'Sin Dirección'),
        COALESCE(NEW.raw_user_meta_data->>'phone', ''),
        NEW.email,
        default_role,
        default_cuota
    );
    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger execution
CREATE OR REPLACE TRIGGER on_auth_user_created
    AFTER INSERT ON auth.users
    FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();
